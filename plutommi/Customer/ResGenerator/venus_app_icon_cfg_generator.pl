#!/usr/bin/perl
use strict;
use warnings;


########################################
# Constant
########################################
my $gcLog           = "[AppIconCfgGen]";

my $gcResPath       ="..\\CustResource";
my $gcAppIconCfgFile= "$gcResPath\\CustVenusAppIconCfg.h";

my $gcSysThemeFile  = "$gcResPath\\VenusSysThemeCFG.xml";

my $gcPreXMLTool    = "..\\..\\..\\tools\\python25\\python.exe ..\\..\\..\\tools\\py\\prexml.py";

my $gcThemeXMLPath   = "ThemeXML";
my $gcThemeXMLTempPath  = "$gcThemeXMLPath\\Temp";
my $gcThemeXMLLogPath   = "$gcThemeXMLPath\\Log";
my $gcThemeXMLLogFile   = "$gcThemeXMLLogPath\\venus_theme_generator.log";


########################################
# Global Variables
########################################


########################################
# Main Function
########################################

# pre-process system theme config file
my $tempXMLFile = "$gcThemeXMLTempPath\\VenusSysThemeCFG.xml.i";
system("$gcPreXMLTool $gcSysThemeFile $tempXMLFile");
die "$gcLog Error: $gcSysThemeFile preprocess error." if ($? != 0);

# load system theme config XML
use XML::Simple;
my $xml = new XML::Simple;
my $XMLData = $xml->XMLin($tempXMLFile, forceArray => 1);

my @gAppIconArray = ();
my %gAppIconEnumArray = ();
foreach(@{$XMLData->{'AppIconPackage'}})
{
    push @gAppIconArray, $_->{'src'};
    $gAppIconEnumArray{$_->{'src'}} = $_->{'enum'};
}

# output app icon configure header file
open OUTF, ">$gcAppIconCfgFile" or die "$gcLog Error: Cannot open $gcAppIconCfgFile";
select OUTF;

print "/\* This file is automatically generated by venus_theme_generator.pl. Do not modify it. \*/\n\n";
my $idx = 0;
foreach(@gAppIconArray)
{
    print "#define $gAppIconEnumArray{$_}\t$idx\n";
    $idx ++;
}

print "\n#define __MMI_COSMOS_APP_ICON_COUNT__\t$idx\n";

close OUTF;

