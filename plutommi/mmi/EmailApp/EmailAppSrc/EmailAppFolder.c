    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    cui_menu_event_struct *menu_evt = (cui_menu_event_struct*)evt;
    EMAIL_MSG_ID msg_id = email_app_acct_fldr_data.curr_msg_id;

