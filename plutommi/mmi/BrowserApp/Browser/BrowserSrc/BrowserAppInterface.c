    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    S32 attr;
    S32 error_code;
    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/
    attr = FS_GetAttributes((WCHAR*) src_file_path);
    attr &= ~FS_ATTR_HIDDEN;
    FS_SetAttributes((WCHAR*) src_file_path, (BYTE) attr);
    srv_brw_bookmarks_modify_filepath_for_save(dest_file_path);

