static STSMM res_lzma_decompress_smm;

static void* mmi_res_lzma_alloc(void* mem_ptr, kal_uint32 mem_size);
static void mmi_res_lzma_free(void* mem_ptr, void* mem_address);

static ISzAlloc mmi_res_lzma_decomp_Alloc = { mmi_res_lzma_alloc, mmi_res_lzma_free };

static void* mmi_res_lzma_alloc(void* mem_ptr, kal_uint32 mem_size)
{
    return smmMallocAligned(&res_lzma_decompress_smm, mem_size, 4);
}

static void mmi_res_lzma_free(void* mem_ptr, void* mem_address)
{
    // Res util will reset the entire buffer every time it decompresses the resource
    // So we do nothing here
}

#ifdef __MMI_RESOURCE_IMAGE_GROUP_COMPRESS__
S32 mmi_res_lzma_decompress_static_buffer(U8 *dest, U32 destLen, const U8 *source, U32 sourceLen)
{
    return mmi_res_lzma_decompress(dest, destLen, source, sourceLen, mmi_res_decomp_static_buff_pool);
}
#endif // __MMI_RESOURCE_IMAGE_GROUP_COMPRESS__

S32 mmi_res_lzma_decompress(U8 *dest, U32 destLen, const U8 *source, U32 sourceLen, void* mmi_res_decomp_buff_pool)
{
    S32 ret;
    U32 decpz_data_size = destLen;
    U32 source_size = sourceLen-13;
    ELzmaStatus status;
    smmInit(&res_lzma_decompress_smm, mmi_res_decomp_buff_pool, RESORCE_LZMA_DECOMPRESS_WORKING_BUFFER_SIZE);

    ret = LzmaDecode((Byte*)dest, &decpz_data_size, ((const Byte*)source)+13, &source_size, 
        (const Byte*)source, LZMA_PROPS_SIZE, LZMA_FINISH_ANY, &status, 
        &mmi_res_lzma_decomp_Alloc);
    if(SZ_OK == ret){
        ASSERT(decpz_data_size == destLen);
        return 0;
    }else{
        return -1;
    }
}

