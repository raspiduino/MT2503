/*****************************************************************************
 * FUNCTION
 *  mmi_sim_prepare_ecc_identifier
 * DESCRIPTION
 *  
 * PARAMETERS
 *  ecc_identifier  [IN]        
 *  ecc_length      [IN]        
 *  ecc_dcs         [IN]        
 *  ecc_output      [OUT]
 * RETURNS
 *  void
 *****************************************************************************/
static void mmi_sim_prepare_ecc_identifier(U8 *ecc_identifier, U8 ecc_length, U8 ecc_dcs, U8 *ecc_output)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    U8 i;
    U8 *tmp_identifier;
    U16 converted_length;
    U16 original_length = ecc_length;

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/
    for (i = 0; i < ecc_length; i++)
    {
        if (*(ecc_identifier + i) == 0xFF)
        {
            *(ecc_identifier + i) = 0;
            original_length = i;
            break;
        }
    }

    tmp_identifier = CovertStringForPlatform(ecc_identifier, original_length, ecc_dcs, &converted_length);
    mmi_ucs2ncpy((S8*) ecc_output,(S8*) tmp_identifier, MMI_PHB_NAME_LENGTH);
    OslMfree(tmp_identifier);
}
#endif /* __REL5__ */


/*****************************************************************************
 * FUNCTION
 *  SimECCInfoSave
 * DESCRIPTION
 *  Callback of PRT_SMU_STARTUP_INFO_IND
 * PARAMETERS
 *  inMsg       [IN]        
 * RETURNS
 *  void
 *****************************************************************************/
U8 SimECCInfoSave(void *info, int mod_src)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    mmi_smu_startup_info_ind_struct *local_data = (mmi_smu_startup_info_ind_struct*) info;

#ifdef __REL5__
    U8 Count = 0;
    U8 gSimEccNameList[(MMI_PHB_NAME_LENGTH + 1) * ENCODING_LENGTH];
#endif /* __REL5__ */

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/
#ifdef __REL5__
    /* update name to applib */
    if (local_data->num > 0)
    {
        for (Count = 0; Count < local_data->num; Count++)
        {
            mmi_sim_prepare_ecc_identifier(local_data->ecc_entry[Count].name, local_data->ecc_entry[Count].name_length, local_data->ecc_entry[Count].name_dcs, gSimEccNameList);
            ecc_custom_update_emergency_number(NULL, 0, ECC_SRC_SIM1 + mod_src - MOD_L4C, 0, gSimEccNameList, (U8) (Count + 1), local_data->num);
        }
    }
#endif /* __REL5__ */

    return (U8)MMI_FALSE;
}


