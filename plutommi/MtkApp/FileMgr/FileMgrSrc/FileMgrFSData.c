#define FSDATA_INSTANCE

S32 mmi_fmgri_fsdata_instance_load_data_async(U8 instance_id,
                        mmi_fmgri_fsdata_instance_load_response_callback rsp_cb,
                        mmi_fmgri_fsdata_instance_load_progress_callback progress_cb,
                        S8* highlight_filename)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    S8 *file_path;
    FMGR_FILTER filter_type;    
    //FMGR_FILE_INFO_STRUCT fileinfo;
    S32 result, load_file_result = 0;

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/

    /* Special case for Drive */
    mmi_fmgri_instance_get_filepath_and_filter(instance_id, &file_path, &filter_type);

    result = mmi_fmgr_fsdata_instance_refresh_drive_list(instance_id);

    if (mmi_ucs2cmp(file_path, (PS8)SRV_FMGR_PATH_ROOT) == 0)
    {
        FMGR_TRACE2(TRC_MMI_FMGR_FFBE33F274F94417A385EDF4A33FE939, 
            "[MMIFMGR] > FSData > mmi_fmgri_fsdata_instance_load_data_async > id=%d, result=%d\n",
            instance_id, result);
        return result;    
    }

    /* Check if instance is changed or filelist is dirty*/
    if ((fmgr_fsdata_p->instance_id != instance_id && fmgr_fsdata_p->instance_id) ||
        ((srv_fmgr_filelist_is_dirty(fmgr_fsdata_p->file_list_hdl) == MMI_TRUE) &&
        (srv_fmgr_filelist_get_state(fmgr_fsdata_p->file_list_hdl) != SRV_FMGR_FILELIST_STATE_REFRESHING) &&
        (srv_fmgr_filelist_get_state(fmgr_fsdata_p->file_list_hdl) != SRV_FMGR_FILELIST_STATE_LOADING)))
    {
        mmi_fmgri_fsdata_instance_release_load(fmgr_fsdata_p->instance_id);
    }
    fmgr_fsdata_p->instance_id = instance_id;

    /* Check if need to create new file list handle */
    if (fmgr_fsdata_p->file_list_hdl == 0)
    {
        result = srv_fmgr_folder_filelist_create((const WCHAR *)file_path,
                        &filter_type,
                        srv_fmgr_sort_get(),
                        NULL, 0,
                        &fmgr_fsdata_p->file_list_hdl);

        if (result < 0 )
        {
            goto error_loading;
        }
    }

    switch (srv_fmgr_filelist_get_state(fmgr_fsdata_p->file_list_hdl))
    {
        case SRV_FMGR_FILELIST_STATE_INIT:
            if (srv_fmgr_filelist_refresh(fmgr_fsdata_p->file_list_hdl,
                    (const WCHAR *)highlight_filename,
                    0,
                    SRV_FMGR_FILELIST_REFRESH_PROGRESS_ON,
                    mmi_fmgr_fsdata_load_file_result_proc,
                    NULL) < 0)
            {
                goto error_loading;
            }
            fmgr_fsdata_p->instance_rsp_cb = rsp_cb;
            fmgr_fsdata_p->instance_progress_cb = progress_cb;
            fmgr_fsdata_p->load_file_index = -1;
            result = SRV_FMGR_FILELIST_ERROR_FILE_NOT_READY;
            break;

        case SRV_FMGR_FILELIST_STATE_REFRESHING:
        case SRV_FMGR_FILELIST_STATE_LOADING:
            result = SRV_FMGR_FILELIST_ERROR_FILE_NOT_READY;
            break;
        
        case SRV_FMGR_FILELIST_STATE_READY:
            if ((srv_fmgr_filelist_get_capability(fmgr_fsdata_p->file_list_hdl) == SRV_FMGR_FILELIST_CAP_LOAD_ON_DEMAND) &&
                (fmgr_fsdata_p->load_file_index != -1))
            {
                load_file_result = srv_fmgr_filelist_load_file(fmgr_fsdata_p->file_list_hdl, 
                    fmgr_fsdata_p->load_file_index,
                    mmi_fmgr_fsdata_load_file_result_proc, NULL);
                if (load_file_result != SRV_FMGR_FILELIST_ERROR_FILE_READY)
                {
                    if (load_file_result !=  0)
                    {   
                        goto error_loading;
                    }
                    fmgr_fsdata_p->instance_rsp_cb = rsp_cb;
                    fmgr_fsdata_p->instance_progress_cb = progress_cb;
                    fmgr_fsdata_p->load_file_index = -1;
                    result = SRV_FMGR_FILELIST_ERROR_FILE_NOT_READY;
                    break;
                }
            }

            /* File list is ready, return file/folder count */
            result = srv_fmgr_filelist_count(fmgr_fsdata_p->file_list_hdl);
        #ifdef __FMGR_HYPERLINK_SUPPORT__
			if (!(FS_DEVICE_TYPE_CARD == FS_GetDevType((WCHAR*)file_path) &&
                FS_GetDevStatus(file_path[0], FS_MOUNT_STATE_ENUM) != FS_NO_ERROR))
            {
                result += mmi_fmgri_instance_get_hyperlink_count(fmgr_fsdata_p->instance_id);
            }            
        #endif /* __FMGR_HYPERLINK_SUPPORT__ */
            break;

        default:
            result = 0;
            break;
    }

error_loading:

    if (result < 0 && result != SRV_FMGR_FILELIST_ERROR_FILE_NOT_READY)
    {
        mmi_fmgri_fsdata_instance_release_load(fmgr_fsdata_p->instance_id);
    }
    
    FMGR_TRACE2(TRC_MMI_FMGR_FFBE33F274F94417A385EDF4A33FE939, 
        "[MMIFMGR] > FSData > mmi_fmgri_fsdata_instance_load_data_async > id=%d, result=%d\n",
        instance_id, result);

    return result;
}

S32 mmi_fmgri_fsdata_instance_release_load(U8 instance_id)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    S32 result = 0;

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/

    if (fmgr_fsdata_p->instance_id != instance_id)
    {
        result = instance_id * 100 + fmgr_fsdata_p->instance_id * 10000;
        FMGR_TRACE1(TRC_MMI_FMGR_68480580CF0642B89C1BB65DAE89B492, 
            "[MMIFMGR] > FSData > mmi_fmgri_fsdata_instance_release_load > id=%d\n", result);
        return 0;
    }

    fmgr_fsdata_p->instance_id = 0;
    fmgr_fsdata_p->instance_progress_cb = NULL;
    fmgr_fsdata_p->instance_rsp_cb = NULL;

    srv_fmgr_filelist_destroy(fmgr_fsdata_p->file_list_hdl);
    fmgr_fsdata_p->file_list_hdl = 0;

    mmi_fmgri_fsdata_unlock_file_list();

    FMGR_TRACE1(TRC_MMI_FMGR_68480580CF0642B89C1BB65DAE89B492, 
        "[MMIFMGR] > FSData > mmi_fmgri_fsdata_instance_release_load > id=%d\n", instance_id);

    return result;
}

mmi_ret mmi_fmgr_fsdata_load_file_result_proc(mmi_event_struct *param)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    srv_fmgr_filelist_refresh_result_event_struct* rsp_evt;
    srv_fmgr_filelist_refresh_progress_event_struct* progress_evt;
    srv_fmgr_filelist_load_result_event_struct* load_evt;
    S32 result = 0, highlight_index = 0;
    U32 count, complete_count = 0;
#ifdef __FMGR_HYPERLINK_SUPPORT__
    U32 link_count;
#endif

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/

    switch (param->evt_id)
    {
        case EVT_ID_SRV_FMGR_FILELIST_REFRESH_RESULT:

            rsp_evt = (srv_fmgr_filelist_refresh_result_event_struct*)param;
            FMGR_ASSERT(rsp_evt->result <= 0);

            if(rsp_evt->result == 0)
            {
                /* Load file success */
                count = rsp_evt->total_count;
                highlight_index = rsp_evt->search_index;
            #ifdef __FMGR_HYPERLINK_SUPPORT__
                link_count = mmi_fmgri_instance_get_hyperlink_count(fmgr_fsdata_p->instance_id);
                count += link_count;
                if (highlight_index != -1)
                {
                    /* Search index found */
                    highlight_index += link_count;
                }
            #endif /* __FMGR_HYPERLINK_SUPPORT__ */
                result = count;
            }
            else
            {
                /* Load file fail */
                result = rsp_evt->result;
            }

            fmgr_fsdata_p->instance_rsp_cb(fmgr_fsdata_p->instance_id, result, highlight_index);
            break;

        case EVT_ID_SRV_FMGR_FILELIST_REFRESH_PROGRESS:

            progress_evt = (srv_fmgr_filelist_refresh_progress_event_struct*)param;
            FMGR_ASSERT(progress_evt->total_count >= 0);

            count = progress_evt->total_count;
            complete_count = progress_evt->completed_count;
        #ifdef __FMGR_HYPERLINK_SUPPORT__
            link_count = mmi_fmgri_instance_get_hyperlink_count(fmgr_fsdata_p->instance_id);
            count += link_count;
            complete_count += link_count;
        #endif /* __FMGR_HYPERLINK_SUPPORT__ */

            fmgr_fsdata_p->instance_progress_cb(fmgr_fsdata_p->instance_id, count, complete_count);
            break;

        case EVT_ID_SRV_FMGR_FILELIST_LOAD_RESULT:

            load_evt = (srv_fmgr_filelist_load_result_event_struct*)param;
            FMGR_ASSERT(load_evt->result <= 0);

            if(load_evt->result == 0)
            {
                /* Load file success */
                count = srv_fmgr_filelist_count(fmgr_fsdata_p->file_list_hdl);
            #ifdef __FMGR_HYPERLINK_SUPPORT__
                count += mmi_fmgri_instance_get_hyperlink_count(fmgr_fsdata_p->instance_id);
            #endif /* __FMGR_HYPERLINK_SUPPORT__ */
                result = count;
            }
            else
            {
                /* Load file fail */
                result = load_evt->result;
            }

            fmgr_fsdata_p->instance_rsp_cb(fmgr_fsdata_p->instance_id, result, -1);
            break;

        default:
             FMGR_ASSERT(0);
    }

    return MMI_RET_OK;
}

