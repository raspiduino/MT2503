S32 mmi_fmgri_fsdata_instance_get_drive_info(U8 instance_id, S32 index, fmgr_drive_info_struct *drv_info)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    S8 *file_path;
    SRV_FMGR_DRVLIST_HANDLE drvlist_hdl;
    mmi_fmgr_instance_struct* instance;

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/

    mmi_fmgri_instance_get_filepath_and_filter(instance_id, &file_path, NULL);

    if (mmi_ucs2cmp(file_path, (PS8)SRV_FMGR_PATH_ROOT))
    {
        return MMI_FMGR_ERROR_INVALID_PARAM;
    }

    if (!drv_info)
    {
        return MMI_FMGR_ERROR_INVALID_PARAM;
    }

    memset(drv_info, 0, sizeof(fmgr_drive_info_struct));

    instance = mmi_fmgri_get_instance_by_id(instance_id);
    drvlist_hdl = srv_fmgr_drivelist_create((srv_fmgr_drivelist_type_enum)(instance->drv_type));

#ifdef __FMGR_CUSTOM_ROOT_SUPPORT__
    if (mmi_fmgri_instance_cr_get_count(instance_id, drvlist_hdl) >= 0)
    {
        if (mmi_fmgri_instance_cr_get_info(instance_id, drvlist_hdl, index, drv_info) < 0)
        {
            return MMI_FMGR_ERROR_INVALID_PARAM;
        }
        return 0;
    }
#endif /* __FMGR_CUSTOM_ROOT_SUPPORT__ */

    drv_info->drv_type = srv_fmgr_drv_get_type(srv_fmgr_drivelist_get_drv_letter(drvlist_hdl, index));

    if(drv_info->drv_type < 0)
    {
        return FS_DRIVE_NOT_FOUND;
    }

    mmi_ucs2cpy((S8*)drv_info->drv_path, (S8*) srv_fmgr_drv_get_path_by_type((srv_fmgr_drv_type_enum)drv_info->drv_type));

    srv_fmgr_drivelist_destroy(drvlist_hdl);

    return 0;
}


