void custom_rec_init_table(void)
{
    kal_uint32 i,j;
    for(i=0; i<MAX_CUSTOM_REC_CONFIG_TBL_CONFIG_NUM ;i++)
    {
        for(j=0; j<MAX_CUSTOM_REC_CONFIG_TBL_SETTING_NUM; j++)
        {
            custom_rec_config_table[i][j]  = SETTING_CONFIG_VALUE;
        }
    }
    for(i=0; i<SETTING_CONFIG_TARGET_END ;i++)
    {
        custom_rec_config_setting_table[i].type = SETTING_CONFIG_TYPE_VALUE;
    }
}



