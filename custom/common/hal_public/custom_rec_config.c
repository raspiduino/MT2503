kal_bool recorder_config_has_dependency(void)
{
    return (kal_bool)(!custom_rec_config_table_free_style);
}

kal_bool recorder_config_is_depended(recoorder_config_setting_target target)
{

    if(custom_rec_config_table_free_style)
        return KAL_FALSE;

    if(SETTING_CONFIG_TARGET_NONE == target)
        return KAL_FALSE;

    return (kal_bool)(custom_rec_config_setting_table[target].type == SETTING_CONFIG_TYPE_DEPENDED);
}


kal_bool recorder_config_is_key(recoorder_config_setting_target target)
{
    if(custom_rec_config_table_free_style)
        return KAL_FALSE;

    if(SETTING_CONFIG_TARGET_NONE == target)
        return KAL_FALSE;

    return (kal_bool)(custom_rec_config_setting_table[target].type == SETTING_CONFIG_TYPE_KEY);
}


recoorder_config_setting_target recorder_config_get_key(void)
{
    if(custom_rec_config_table_free_style)
        return SETTING_CONFIG_TARGET_NONE;

    if(!custom_rec_config_table_free_style)
    {
        return (recoorder_config_setting_target)(custom_rec_config_table_key_col);
    }

    return SETTING_CONFIG_TARGET_NONE;

}

recoorder_config_setting_value recorder_config_get_default_key_value(void)
{
    return custom_rec_config_table[0][custom_rec_config_table_key_col];
}


void recorder_config_get_setting_set(recoorder_config_setting_value key_value, recoorder_config_setting_set* set)
{
    kal_uint32 row_idx = 0;
    kal_uint32 i;

    for(i=0; i<custom_rec_config_table_real_row_num; i++)
    {
        if(custom_rec_config_table[i][custom_rec_config_table_key_col] == key_value)
        {
            row_idx = i;
        }
    }

    set->count = 0;
    for(i=0; i<SETTING_CONFIG_TARGET_END; i++)
    {
        if(custom_rec_config_setting_table[i].type == SETTING_CONFIG_TYPE_DEPENDED)
        {
            set->target[set->count] = (recoorder_config_setting_target)i;
            set->value[set->count] = custom_rec_config_table[row_idx][custom_rec_config_setting_table[i].index];
            set->count++;
        }
    }

}


recoorder_config_setting_value recorder_config_get_setting(recoorder_config_setting_value key_value, recoorder_config_setting_target target)
{
    kal_uint32 row_idx = 0;
    kal_uint32 i;
    kal_uint32 not_depended_setting = 0;

    for(i=0; i<custom_rec_config_table_real_row_num; i++)
    {
        if(custom_rec_config_table[i][custom_rec_config_table_key_col] == key_value)
        {
            row_idx = i;
        }
    }

    if(custom_rec_config_setting_table[target].type == SETTING_CONFIG_TYPE_DEPENDED)
    {
        return custom_rec_config_table[row_idx][custom_rec_config_setting_table[target].index];
    }

    ASSERT(not_depended_setting);
	return SETTING_CONFIG_VALUE;

}